fun checkPersonAge (petAge : Double?): String {
    return if (petAge!! <= 0.0) {
        "Ошибка ввода возраста собаки"
    } else (if (petAge > 7.0) {
        "Ошибка ввода возраста собаки"
    } else {
        convertPersonAge(petAge)
    }) as String
}

fun convertPersonAge (petAge : Double?): Double {
    return if (petAge!! > 0.0 && petAge < 2.0) {
        10.5 * petAge
    } else {
        4.0 * petAge
    }
}

