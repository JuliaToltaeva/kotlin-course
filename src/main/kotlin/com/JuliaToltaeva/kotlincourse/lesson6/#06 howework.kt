fun getSeason (monthNum : Int?): String {
    return when {
        (monthNum!! < 1) || (monthNum > 12) -> "Некорректно введен параметр Номер месяца"
        monthNum == 12 -> "Зима"
        monthNum in 1..2 -> "Зима"
        monthNum in 3..5 -> "Весна"
        monthNum in 6..8 -> "Лето"
        else -> "Осень"
    }
    }

